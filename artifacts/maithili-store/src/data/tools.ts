export function getToolBySlug(slug: string): Tool | undefined {
  return tools.find((t) => t.slug === slug);
}

export function getToolsByCategory(category: ToolCategory): Tool[] {
  return tools.filter((t) => t.category === category);
}

export function getToolsByAudience(audience: Audience): Tool[] {
  return tools.filter((t) => t.audiences.includes(audience));
}

export function getRelatedTools(tool: Tool, limit = 6): Tool[] {
  return tools
    .filter((t) => t.slug !== tool.slug && t.category === tool.category)
    .slice(0, limit);
}

export function getNewArrivals(limit = 8): Tool[] {
  return [...tools]
    .sort((a, b) => (a.addedAt < b.addedAt ? 1 : -1))
    .slice(0, limit);
}

export function getPopularTools(limit = 6): Tool[] {
  return tools.filter((t) => t.tags.includes("popular")).slice(0, limit);
}
